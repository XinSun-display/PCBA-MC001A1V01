//********* page:backlight *********
void button_backlightOnOff_clicked(lv_event_t * e)
{
    lv_event_code_t event_code = lv_event_get_code(e);

    static uint8_t backlight_status = 1;

    if(event_code == LV_EVENT_CLICKED) {
        lcd_backlight_onoff(!backlight_status);
        backlight_status = !backlight_status;
    }
}

void slider_backlight_value_changed(lv_event_t * e)
{
    #if BACKLIGHT_PWM_ENABLE
    lv_event_code_t event_code = lv_event_get_code(e);
    lv_obj_t * slider = lv_event_get_target_obj(e);
    
        if(event_code == LV_EVENT_VALUE_CHANGED) {
            lcd_backlight_set_pwm_duty(lv_slider_get_value(slider));
        }
    #endif
}
