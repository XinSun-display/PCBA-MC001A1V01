void Button_Backlight_clicked(lv_event_t * e)
{
    lv_event_code_t event_code = lv_event_get_code(e);

    if(event_code == LV_EVENT_CLICKED) 
    {
        #if BACKLIGHT_PWM_ENABLE
        lv_obj_add_flag(ui_ButtonBacklightONOFF, LV_OBJ_FLAG_HIDDEN);
        #else
        lv_obj_add_flag(ui_SliderBacklight, LV_OBJ_FLAG_HIDDEN);
        #endif

        _ui_screen_change(&ui_backlight, LV_SCR_LOAD_ANIM_FADE_ON, 0, 0, &ui_backlight_screen_init);
    }
}
